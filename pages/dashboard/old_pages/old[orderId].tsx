import { Order } from "@prisma/client";
import type { GetServerSideProps, NextPage } from "next";
import { unstable_getServerSession } from "next-auth";
import Head from "next/head";
import prisma from "../../../constants/prisma";
import { authOptions } from "../../api/auth/[...nextauth]";

type TransactionProps = {
  order: Order | null;
};

const OrderPage: NextPage<TransactionProps> = ({ order }: TransactionProps) => {
  if (order == null) {
    return (
      <div className="w-full h-screen bg-black flex overflow-hidden">
        <Head>
          <title>transaction Not Found</title>
          <meta name="description" content="Generated by create next app" />
          <link rel="icon" href="/favicon.ico" />
        </Head>
        <main className="text-xl text-white flex items-center">
          <h1 className="mx-auto">This transaction does not exist</h1>
        </main>
      </div>
    );
  }

  return (
    <div className="w-screen h-screen bg-black flex overflow-hidden">
      <Head>
        <title>Find your perfect setup</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className="flex flex-col container mx-auto my-auto items-center py-4 text-white">
        <h1>{`Transaction: ${order.id}`}</h1>
        <div>{`Transaction was placed on ${order.createdAt}`}</div>
        <div>{`Transaction status is ${order.status.toLowerCase()}`}</div>
        <div>{`Transaction amount was $${order.amount}`}</div>
      </main>
    </div>
  );
};

export const getServerSideProps: GetServerSideProps = async (context) => {
  const { orderId } = context.query;
  const session = await unstable_getServerSession(
    context.req,
    context.res,
    authOptions
  );
  if (session && typeof orderId == "string") {
    const order = await prisma.order.findUnique({
      where: {
        id: orderId,
      },
    });

    return {
      props: {
        order: JSON.parse(JSON.stringify(order ?? null)),
      },
    };
  }
  return { props: {} };
};

export default OrderPage;
